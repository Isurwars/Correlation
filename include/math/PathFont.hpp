  std::string render(const std::string &text, double x, double y, double size,
                     const std::string &anchor = "start") {
    double scale = size / 20.0;
    std::vector<uint32_t> codes = utf8ToUnicode(text);

    double total_width = 0;
    for (uint32_t c : codes) {
      if (glyphs_.count(c)) {
        total_width += (glyphs_[c].right - glyphs_[c].left) * scale;
      } else if (c == ' ') {
        total_width += 7 * scale;
      }
    }

    double cur_x = x;
    if (anchor == "middle")
      cur_x -= total_width / 2.0;
    else if (anchor == "end")
      cur_x -= total_width;

    std::string path_data;
    for (uint32_t c : codes) {
      if (c == ' ') {
        cur_x += 7 * scale;
        continue;
      }
      if (glyphs_.count(c)) {
        const auto &g = glyphs_[c];
        double offset_x = cur_x - g.left * scale;
        for (const auto &stroke : g.strokes) {
          for (size_t i = 0; i < stroke.size(); ++i) {
            double px = offset_x + stroke[i].first * scale;
            double py = y + (stroke[i].second - 10) * scale;
            if (i == 0)
              path_data += std::format("M {:.2f} {:.2f} ", px, py);
            else
              path_data += std::format("L {:.2f} {:.2f} ", px, py);
          }
        }
        cur_x += (g.right - g.left) * scale;
      }
    }

    return path_data;
  }

